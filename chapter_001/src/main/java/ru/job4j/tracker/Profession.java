package ru.job4j.tracker;

public class Profession {
    public String name;

    public String surname;

    public String education;

    public String birthday;

    public String getName(Profession profession) {
        return name;
    }

    public String getSurname(Profession profession) {
        return surname;
    }

    public String getEducation(Profession profession) {
        return education;
    }

    public String gerBirthday(Profession profession) {
        return birthday;
    }
}
